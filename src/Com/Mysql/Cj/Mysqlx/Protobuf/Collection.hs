instance P'.TextType Collection where
  tellT = P'.tellSubMessage
  getT = P'.getSubMessage

instance P'.TextMsg Collection where
  textPut msg
   = do
       P'.tellT "name" (name msg)
       P'.tellT "schema" (schema msg)
  textGet
   = do
       mods <- P'.sepEndBy (P'.choice [parse'name, parse'schema]) P'.spaces
       Prelude'.return (Prelude'.foldl (\ v f -> f v) P'.defaultValue mods)
    where
        parse'name
         = P'.try
            (do
               v <- P'.getT "name"
               Prelude'.return (\ o -> o{name = v}))
        parse'schema
         = P'.try
            (do
               v <- P'.getT "schema"
               Prelude'.return (\ o -> o{schema = v}))