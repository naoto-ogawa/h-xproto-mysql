instance P'.TextType Column where
  tellT = P'.tellSubMessage
  getT = P'.getSubMessage

instance P'.TextMsg Column where
  textPut msg
   = do
       P'.tellT "name" (name msg)
       P'.tellT "alias" (alias msg)
       P'.tellT "document_path" (document_path msg)
  textGet
   = do
       mods <- P'.sepEndBy (P'.choice [parse'name, parse'alias, parse'document_path]) P'.spaces
       Prelude'.return (Prelude'.foldl (\ v f -> f v) P'.defaultValue mods)
    where
        parse'name
         = P'.try
            (do
               v <- P'.getT "name"
               Prelude'.return (\ o -> o{name = v}))
        parse'alias
         = P'.try
            (do
               v <- P'.getT "alias"
               Prelude'.return (\ o -> o{alias = v}))
        parse'document_path
         = P'.try
            (do
               v <- P'.getT "document_path"
               Prelude'.return (\ o -> o{document_path = P'.append (document_path o) v}))