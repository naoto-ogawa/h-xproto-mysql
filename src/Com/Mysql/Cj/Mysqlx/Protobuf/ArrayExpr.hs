data ArrayExpr = ArrayExpr{value :: !(P'.Seq Com.Mysql.Cj.Mysqlx.Protobuf.Expr)}
               deriving (Prelude'.Show, Prelude'.Eq, Prelude'.Ord, Prelude'.Typeable, Prelude'.Data, Prelude'.Generic)

instance P'.Mergeable ArrayExpr where
  mergeAppend (ArrayExpr x'1) (ArrayExpr y'1) = ArrayExpr (P'.mergeAppend x'1 y'1)

instance P'.Default ArrayExpr where
  defaultValue = ArrayExpr P'.defaultValue

instance P'.Wire ArrayExpr where
  wireSize ft' self'@(ArrayExpr x'1)
   = case ft' of
       10 -> calc'Size
       11 -> P'.prependMessageSize calc'Size
       _ -> P'.wireSizeErr ft' self'
    where
        calc'Size = (P'.wireSizeRep 1 11 x'1)
  wirePut ft' self'@(ArrayExpr x'1)
   = case ft' of
       10 -> put'Fields
       11 -> do
               P'.putSize (P'.wireSize 10 self')
               put'Fields
       _ -> P'.wirePutErr ft' self'
    where
        put'Fields
         = do
             P'.wirePutRep 10 11 x'1
  wireGet ft'
   = case ft' of
       10 -> P'.getBareMessageWith update'Self
       11 -> P'.getMessageWith update'Self
       _ -> P'.wireGetErr ft'
    where
        update'Self wire'Tag old'Self
         = case wire'Tag of
             10 -> Prelude'.fmap (\ !new'Field -> old'Self{value = P'.append (value old'Self) new'Field}) (P'.wireGet 11)
             _ -> let (field'Number, wire'Type) = P'.splitWireTag wire'Tag in P'.unknown field'Number wire'Type old'Self

instance P'.MessageAPI msg' (msg' -> ArrayExpr) ArrayExpr where
  getVal m' f' = f' m'

instance P'.GPB ArrayExpr

